package model;

/**
 *
 * @author lap
 */
public class Comment {

    private String id;
    private String content;
    private String date;
    private int rating;
    private Item item;
    private User user;

    public Comment() {
    }

    public Comment(String id, String content, String date, int rating, Item item, User user) {
        this.id = id;
        this.content = content;
        this.date = date;
        this.rating = rating;
        this.item = item;
        this.user = user;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public Item getItem() {
        return item;
    }

    public void setItem(Item item) {
        this.item = item;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

}

    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User u = (User) session.getAttribute("account");
        OrderDAO odb = new OrderDAO();
        ArrayList<Order> list = odb.getListOrderByUser(u);
        if (!list.isEmpty()) {
            for (Order x : list) {
                String newDate = formatDateDB(x.getOrderDate());
                x.setOrderDate(newDate);
            }
        }
        Collections.reverse(list);
        request.setAttribute("listOrder", list);
        request.getRequestDispatcher("purchase.jsp").forward(request, response);
    }

    private String formatDateDB(String dateString) {
        SimpleDateFormat inputFormat = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat outputFormat = new SimpleDateFormat("dd MMMM yyyy", new Locale("en", "EN"));

        Date date = new Date();
        try {
            date = inputFormat.parse(dateString);
        } catch (ParseException ex) {
            System.out.println(ex);
        }
        String formattedDate = outputFormat.format(date);
        return formattedDate;
    }

